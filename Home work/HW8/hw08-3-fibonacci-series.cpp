#include <stdio.h>

int main() {
    int n;
    
    // รับค่าจำนวนที่ต้องการจากผู้ใช้
    scanf("%d", &n);
    
    // ตัวแปรสำหรับเก็บค่าของอนุกรม
    int a = 1, b = 1, next;
    
    // แสดงผลลัพธ์ของอนุกรม
    printf("Series = %d + %d", a, b);  // แสดงสองตัวแรก
    
    // ผลรวมเริ่มต้น
    int sum = a + b;
    
    // คำนวณค่าของฟีโบนัชชีถัดไปและแสดงผล
    for (int i = 3; i <= n; i++) {
        next = a + b;  // คำนวณค่าถัดไปในอนุกรม
        printf(" + %d", next);  // แสดงตัวเลขถัดไป
        sum += next;  // เพิ่มค่าถัดไปในผลรวม
        
        // ปรับค่าของ a และ b ให้เหมาะสมสำหรับการคำนวณลำดับถัดไป
        a = b;
        b = next;
    }
    
    // แสดงผลรวม
    printf("\nSum = %d\n", sum);
    
    return 0;
}
