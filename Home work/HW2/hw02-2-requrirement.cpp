#include <stdio.h>

int main() {
    char response;

    printf(">> เริ่มต้นกระบวนการรับงานทำเว็บไซต์ <<\n");

    // ขั้นตอนแรก: โพสต์หางาน
    do {
        printf("โพสต์หางานทำเว็บไซต์ (y/n): ");
        scanf(" %c", &response);

        if (response == 'n') {
            printf("โพสต์หางานก่อน (y/n): ");
            scanf(" %c", &response);
        }
    } while (response == 'n'); // วนลูปหากตอบ 'n'

   do {
        printf("มีลูกค้าสนใจติดต่อมาหรือยัง? (y/n): ");
        scanf(" %c", &response);

        if (response == 'n') {
            printf("มีลูกค้าสนใจติดต่อมาหรือยัง? (y/n): ");
            scanf(" %c", &response);
        }
    } while (response == 'n'); // วนลูปหากตอบ 'n'

    while (response == 'y') {
        printf(">> เริ่มคุยกับลูกค้าเพื่อคัดกรองความต้องการ <<\n");

        // ขั้นตอนการพูดคุยกับลูกค้า
        do {
            printf(">> ลูกค้าต้องการเว็บไซต์ประเภทไหน? (y/n): ");
            scanf(" %c", &response);

            if (response == 'n') {
                printf("** แนะนำ: ถามลูกค้าเพิ่มเติมเกี่ยวกับความต้องการฟีเจอร์เฉพาะของเว็บไซต์\n");
            }
        } while (response == 'n');  // ถ้าตอบ no จะวนกลับมาถามคำถามนี้อีกครั้ง

        do {
            printf(">> อธิบายระยะเวลาในการทำงาน (y/n): ");
            scanf(" %c", &response);

            if (response == 'n') {
                printf("** แนะนำ: แจ้งระยะเวลาในการทำงานให้ลูกค้าทราบล่วงหน้า เพื่อหลีกเลี่ยงการเข้าใจผิด\n");
            }
        } while (response == 'n');  // ถ้าตอบ no จะวนกลับมาถามคำถามนี้อีกครั้ง

        do {
            printf(">> กำหนดราคาและเงื่อนไขการทำงาน (y/n): ");
            scanf(" %c", &response);

            if (response == 'n') {
                printf("** แนะนำ: คุยเรื่องราคาและเงื่อนไขให้ชัดเจนก่อนเริ่มทำงาน\n");
            }
        } while (response == 'n');  // ถ้าตอบ no จะวนกลับมาถามคำถามนี้อีกครั้ง

        do {
            printf(">> ลูกค้าจ่ายเงินมัดจำ (y/n): ");
            scanf(" %c", &response);

            if (response == 'n') {
                printf("** แนะนำ: ต้องให้ลูกค้าจ่ายมัดจำก่อนเริ่มงานเสมอ เพื่อความปลอดภัยทั้งสองฝ่าย\n");
            }
        } while (response == 'n');  // ถ้าตอบ no จะวนกลับมาถามคำถามนี้อีกครั้ง

        printf(">> เริ่มต้นทำเว็บไซต์ตามข้อตกลง\n");
        printf(">> รายงานความคืบหน้าให้ลูกค้าทราบเป็นระยะ (y/n): ");
        scanf(" %c", &response);

        if (response == 'y') {
            printf(">> เว็บไซต์เสร็จสมบูรณ์แล้วหรือไม่? (y/n): ");
            scanf(" %c", &response);

            if (response == 'y') {
                printf(">> ลูกค้าจ่ายเงินที่เหลือ\n");
                printf(">> ส่งมอบเว็บไซต์ให้ลูกค้าเรียบร้อย\n");
                printf(">> จบงาน รับความคิดเห็นจากลูกค้า\n");
                break;
            } else {
                printf("** แนะนำ: ส่งงานตามที่ทำเสร็จให้ลูกค้าตรวจสอบและขอแก้ไข\n");
            }
        } else {
            printf("** แนะนำ: ขอให้ลูกค้าให้ข้อมูลเพิ่มเติมเกี่ยวกับความคืบหน้าของงาน\n");
        }
    }

    return 0;
}